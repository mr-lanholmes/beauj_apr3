package workshop01.model;

public class Item {

	private String item;
	private Integer quantity;

	//Item property
	public String getItem() {
		return item;
	}
	public void setItem(String item) {
		this.item = item;
	}

	//Quantity property
	public Integer getQuantity() {
		return quantity;
	}
	public void setQuantity(Integer quantity) {
		this.quantity = quantity;
	}

	
}
